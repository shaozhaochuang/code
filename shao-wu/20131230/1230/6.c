//3、三对新人举行婚礼，新郎分别是A，B，C，新娘分别是x，y，z。有人想知道他们究竟谁和谁结婚，于是就问其中三人，A说他将和x结婚，x说她的未婚夫是C，C说他将和z结婚。那人就知道他们全都在开玩笑，说的全是假话。请编程得出究竟谁和谁结婚。

#include <stdio.h>

int main(void)
{
	int a, b, c;
	for (a = 1; a <= 3; a++) {
		for (b = 1; b <= 3; b++) {
			for (c = 1; c <= 3; c++) {
				if ( (a!=1) && (c!=1) && (c!=3) && (a!=b) && (a!=c) && (b!=c)) {
					printf("%c将和A结婚!\n", 'x' + a - 1);
					printf("%c将和B结婚!\n", 'x' + b - 1);
					printf("%c将和C结婚!\n", 'x' + c - 1);
				}
			}
		}
	}

	return 0;
}
